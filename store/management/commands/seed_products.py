class Command(BaseCommand):
    help = 'Seed the database with realistic market products for the storefront catalog.'

    def handle(self, *args, **options):
        created_count = 0
        updated_count = 0

        for product_data in PRODUCTS:
            slug = product_data['slug']
            defaults = {key: value for key, value in product_data.items() if key != 'slug'}

            _, created = Product.objects.update_or_create(slug=slug, defaults=defaults)
            if created:
                created_count += 1
            else:
                updated_count += 1

        self.stdout.write(
            self.style.SUCCESS(
                f'Seed complete. Created: {created_count}, Updated: {updated_count}, Total: {Product.objects.count()}.'
            )
        )
